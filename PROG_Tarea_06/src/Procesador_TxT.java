    private void menu_item_abrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menu_item_abrirActionPerformed
        JFileChooser jf = new JFileChooser();
        jf.showOpenDialog(this);
        File archivo = jf.getSelectedFile();
        ruta = archivo.getAbsolutePath();
        try {
            BufferedReader br = new BufferedReader(new FileReader(ruta));
            try {
                texto = br.readLine();
                String linea;
                do {
                    linea = br.readLine();
                    if (linea != null) {
                        texto = texto + linea;
                    }
                } while (linea != null);
                this.panel_txt.setText(texto);
            } catch (IOException ex) {
                Logger.getLogger(Procesador_TxT.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Procesador_TxT.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_menu_item_abrirActionPerformed

    private void menu_item_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menu_item_guardarActionPerformed
        JFileChooser jf = new JFileChooser();
        jf.showOpenDialog(this);
        File archivo = jf.getSelectedFile();
        ruta = archivo.getAbsolutePath();
        System.out.println(ruta);
        try {
            BufferedWriter bw = new BufferedWriter(new FileWriter(ruta));
            texto = this.panel_txt.getText();
            bw.write(texto);
            bw.close();
        } catch (IOException ex) {
            Logger.getLogger(Procesador_TxT.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_menu_item_guardarActionPerformed

